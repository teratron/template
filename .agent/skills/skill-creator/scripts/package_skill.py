#!/usr/bin/env python3
"""
Skill Packager - Creates a distributable .skill file of a skill folder

Usage:
    python scripts/package_skill.py <path/to/skill-folder> [output-directory]
"""

import sys
import zipfile
from pathlib import Path

# Add script directory to path to import quick_validate
sys.path.append(str(Path(__file__).parent))
from quick_validate import validate_skill


def package_skill(skill_path, output_dir=None):
    """
    Package a skill folder into a .skill file.
    """
    skill_path = Path(skill_path).resolve()

    # Validate skill folder exists
    if not skill_path.exists():
        print(f"‚ùå Error: Skill folder not found: {skill_path}")
        return None

    if not skill_path.is_dir():
        print(f"‚ùå Error: Path is not a directory: {skill_path}")
        return None

    # Run validation before packaging
    print("üîç Validating skill...")
    valid, message = validate_skill(skill_path)
    if not valid:
        print(f"‚ùå Validation failed: {message}")
        print("   Please fix the validation errors before packaging.")
        return None
    print(f"‚úÖ {message}\n")

    # Determine output location
    skill_name = skill_path.name
    if output_dir:
        output_path = Path(output_dir).resolve()
        output_path.mkdir(parents=True, exist_ok=True)
    else:
        output_path = Path.cwd()

    skill_filename = output_path / f"{skill_name}.skill"

    # Create the .skill file (zip format)
    try:
        with zipfile.ZipFile(skill_filename, "w", zipfile.ZIP_DEFLATED) as zipf:
            for file_path in skill_path.rglob("*"):
                if file_path.is_file():
                    # Calculate the relative path within the zip
                    arcname = file_path.relative_to(skill_path.parent)
                    zipf.write(file_path, arcname)
                    print(f"  Added: {arcname}")

        print(f"\n‚úÖ Successfully packaged skill to: {skill_filename}")
        return skill_filename

    except Exception as e:
        print(f"‚ùå Error creating .skill file: {e}")
        return None


def main():
    if len(sys.argv) < 2:
        print(
            "Usage: python scripts/package_skill.py <path/to/skill-folder> [output-directory]"
        )
        sys.exit(1)

    skill_path = sys.argv[1]
    output_dir = sys.argv[2] if len(sys.argv) > 2 else None

    print(f"üì¶ Packaging skill: {skill_path}")
    result = package_skill(skill_path, output_dir)

    if result:
        sys.exit(0)
    else:
        sys.exit(1)


if __name__ == "__main__":
    main()
